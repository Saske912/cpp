#include <iostream>
#include <string>
#include <fstream>
#include <cstring>
#ifndef STR_LEN
# define STR_LEN 150
#endif

void	from_terminal(void)
{
	char	*str;
	
	if (!(str = new char[STR_LEN]))
	{
		perror("new: str");
		exit(1);
	}
	while (1)
	{
		std::cin.getline(str, STR_LEN);
		if (std::cin.eof())
		{
			break ;
		}
		else
			std::cout << str << std::endl;
	}
	delete [] str;
}

void	get_file(char *arg)
{
	std::ifstream	stream;
	std::string		line;

	stream.open(arg);
	if (stream.is_open())
	{
		while (stream.good())
		{
			std::getline(stream, line);
			std::cout << line << std::endl;
		}
	}
	else
		std::cout << "cat: " << arg << ": No such file or directory" << std::endl;
}

void	from_files(int ac, char **av)
{
	int		i;

	i = 1;
	while (i < ac)
	{
		get_file(av[i]);
		i++;
	}
}

int		main(int ac, char **av)
{
	if (ac == 1)
	{
		from_terminal();
	}
	else if (ac > 1)
	{
		from_files(ac, av);
	}
}