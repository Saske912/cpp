#include "Crappy.hpp"

t_args  Crappy::get_string( void ) const
{
    return this->data;
}

void    Crappy::put_string( t_args *args )
{
    this->data = *args;
}

Crappy::Crappy(void)
{
    t_args  data;

    data = this->get_string();
    data.index = 0;
    this->put_string(&data);
    return ;
}

Crappy::~Crappy(void)
{
    return ;
}
