#include <iostream>
#include "Fixed.hpp"
#include "header.h"
#include <string>

int     main( int ac, char **av )
{
    std::string     arg;
    int             cnt;
    int             tmp;

    if (ac == 2)
    {
        arg = av[1];
        if ((cnt = get_count_brackets(arg)) == -1)
        {
            std::cerr << "Invalid expression (brackets)" << std::endl;
            exit(1);
        }
        tmp = 0;
        while (tmp < cnt)
        {
            eval_brackets(arg);
            tmp++;
        }
        arg = parse(arg);
        std::cout << arg << std::endl;
    }
    else
        std::cerr << "Please insert 1 argument with math expression" << std::endl;
    return 0;
}