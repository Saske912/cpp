#include "Human.hpp"
#include <string>

#define VAL 48
#define MOD 3

int		main(int ac, char **av)
{
	int				k;
	std::string		crocodile("crocodile");
	std::string		lyon("lyon");
	Human			warrior;
	std::string		action;

	if (ac == 2)
		k = atoi(av[1]);
	else
		k = 10;
	while (k--)
	{
		action[0] = (char)k % MOD + VAL;
		warrior.action(action, crocodile);
		warrior.action(action, lyon);
	}
}