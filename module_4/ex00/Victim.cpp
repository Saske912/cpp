#include "Victim.hpp"
#include <iostream>

Victim::Victim( ) {
}

Victim::Victim( std::string name ) : _name(name) {
    std::cout << "Some random victim called " << this->_name;
    std::cout << " Just appeared!" << std::endl;
}

Victim::~Victim( ) {
    std::cout << "Victim " << this->_name;
    std::cout << " Just died for no apparent reason!" << std::endl;
}

Victim::Victim( Victim const &src ) {
    *this = src;
}

Victim &Victim::operator=( Victim const &rhs ) {
    this->_name = rhs._name;
    return *this;
}

std::string Victim::getName() const
{
    return this->_name;
}

std::ostream &  operator<<(std::ostream & o, Victim const & src)
{
    o << "I am " << src.getName();
    o << ", and I like otters!" << std::endl;
    return o;
}

void    Victim::getPolymorphed() const
{
    std::cout << this->getName() << " has been turned into ";
    std::cout << "a cute little sheep!" << std::endl;
}

void    Victim::setName(std::string name)
{
    this->_name = name;
}