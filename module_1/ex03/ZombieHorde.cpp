#include "ZombieHorde.hpp"
# include <cstdlib>

ZombieHorde::ZombieHorde( int n, t_horde horde ) : _horde(horde)
{
    this->_army = new Zombie[n];
	for (int i = 0; i < n; i++)
	{
		this->setZombie(&this->_army[i]);
	}
}

ZombieHorde::~ZombieHorde()
{
    delete [] this->_army;
}

void	ZombieHorde::setZombie( Zombie *zombie )
{
    zombie->setType(this->_horde.types[rand() % this->_horde.cnt_t]);
	zombie->setName(this->_horde.names[rand() % this->_horde.cnt_n]);
	zombie->announce();
}