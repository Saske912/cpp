#include "MiningBarage.hpp"
#include <iostream>

MiningBarage::MiningBarage( void ) : _cnt(0), _max(4) {
    this->_items.mt = 0;
    this->_items.next = 0;
    this->_items.prev = 0;
}

MiningBarage::~MiningBarage( void ) {
    utils::free_items( this->_items.next);
}

MiningBarage::MiningBarage( MiningBarage const &src ) : _max(4) {
    *this = src;
}

MiningBarage &MiningBarage::operator=( MiningBarage const &rhs ) {
    (void)rhs;
    return *this;
}

void    MiningBarage::equip(IMiningLaser* laser)
{
    t_quad *tmp;
    int     i;

    if (!laser)
        return;
    if ( this->_cnt == this->_max)
    {
        delete laser;
        return;
    }
    i = 0;
    tmp = &this->_items;
    while (i < this->_cnt)
    {
        tmp = tmp->next;
        i++;
    }
    tmp->next = utils::new_item(tmp);
    tmp = tmp->next;
    tmp->mt = laser;
    this->_cnt++;
}

void    MiningBarage::mine(IAsteroid * aster) const
{
    t_quad *tmp;

    tmp = this->_items.next;
    for (int i=0;i< this->_cnt;i++)
    {
        tmp->mt->mine(aster);
        tmp = tmp->next;
    }
}