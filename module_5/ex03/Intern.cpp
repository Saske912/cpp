#include "Intern.hpp"
#include <iostream>

Intern::Intern( void )
{
    this->_accepted_forms[0] = "robotomy request";
    this->_accepted_forms[1] = "presidential pardon";
    this->_accepted_forms[2] = "shrubbery creation";
    this->_arr[0] = new RobotomyRequestForm("default");
    this->_arr[1] = new PresidentialPardonForm("default");
    this->_arr[2] = new ShrubberyCreationForm("default");
}

Intern::~Intern( void ) {
    delete this->_arr[0];
    delete this->_arr[1];
    delete this->_arr[2];
}

Intern::Intern( Intern const &src ) {
    *this = src;
}

Intern &Intern::operator=( Intern const &rhs ) {
    (void )rhs;
    return *this;
}

Form*    Intern::makeForm(std::string form_name, std::string target)
{
    for(int i = 0;i < 3; i++)
    {
        if (_accepted_forms[i] == form_name)
        {
            std::cout << "Intern creates " << this->_arr[i]->getName() << std::endl;
            this->_arr[i]->setTarget(target);
            return this->_arr[i]->clone(target);
        }
    }
    std::cout << "This form is unknown" << std::endl;
    throw std::exception();
}