#include <iostream>
#include "Fixed.hpp"

int     main( void )
{
    Fixed a;
    Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );
    Fixed c( Fixed( 5.05f ) / Fixed( 2 ) );
    Fixed d( Fixed( 5.05f ) + Fixed( 2 ) );
    Fixed e( Fixed( 5.05f ) - Fixed( 2 ) );


    std::cout << a << std::endl;
    std::cout << ++a << std::endl;
    std::cout << a << std::endl;
    std::cout << a++ << std::endl;
    std::cout << a << std::endl;
    std::cout << "multiple " << b << std::endl;
    std::cout << "division " << c << std::endl;
    std::cout << "summa " << d << std::endl;
    std::cout << "minus " << e << std::endl;
    std::cout << Fixed::max( a, b ) << std::endl;
    std::cout << Fixed::min( a, b ) << std::endl;
    std::cout << (e + d) * c << std::endl;
    return 0;
}