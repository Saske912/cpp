#include "TacticalMarine.hpp"
#include "AssaultTerminator.hpp"
#include "Squad.hpp"
#include <iostream>

int main()
{
    std::cout << "HEAP:" << std::endl;
    ISpaceMarine* bob = new TacticalMarine;
    ISpaceMarine* jim = new AssaultTerminator;
    ISpaceMarine* ken = jim->clone();
    ISpaceMarine* leak = bob->clone();
    ISquad* vlc = new Squad;
    vlc->push(bob);
    vlc->push(jim);
    for (int i = 0; i < vlc->getCount(); ++i)
    {
        ISpaceMarine* cur = vlc->getUnit(i);
        cur->battleCry();
        cur->rangedAttack();
        cur->meleeAttack();
    }
    delete vlc;
    std::cout << "STACK:" << std::endl;
    Squad   tmp;
    tmp.push(ken);
    tmp.push(leak);
    for (int i = 0; i < tmp.getCount(); ++i)
    {
        ISpaceMarine* cur = tmp.getUnit(i);
        cur->battleCry();
        cur->rangedAttack();
        cur->meleeAttack();
    }
    Squad   copy;
    ISpaceMarine    *k1 = new TacticalMarine;
    copy.push(k1);
    ISpaceMarine    *k2 = k1->clone();
    copy = tmp;
    std::cout << "ASSIGN:" << std::endl;
    for (int i = 0; i < copy.getCount(); ++i)
    {
        ISpaceMarine* cur = copy.getUnit(i);
        cur->battleCry();
        cur->rangedAttack();
        cur->meleeAttack();
    }
    Squad copy2(copy);
    copy2.push(k2);
    std::cout << "COPY: " << std::endl;
    for (int i = 0; i < copy2.getCount(); ++i)
    {
        ISpaceMarine* cur = copy2.getUnit(i);
        cur->battleCry();
        cur->rangedAttack();
        cur->meleeAttack();
    }
    return 0;
}