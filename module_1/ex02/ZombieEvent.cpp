#include "ZombieEvent.hpp"
#include <string>

ZombieEvent::ZombieEvent( int count, t_pool values )
{
	Zombie	*tmp;

	this->_values = values;
	while (count--)
	{
		tmp = this->randomChump();
		tmp->problems();
		delete tmp;
	}
}

ZombieEvent::ZombieEvent( t_pool values ) : _values(values)
{
}

ZombieEvent::~ZombieEvent( void )
{
}

void	ZombieEvent::setZombieType( Zombie *zombie, std::string type )
{
    zombie->setType(type);
}

Zombie*	ZombieEvent::newZombie(std::string name)
{
	Zombie	*zombie = new Zombie(name);
    setZombieType(zombie, this->_values.types[rand() % this->_values.cnt_t]);
	return zombie;
}

Zombie*	ZombieEvent::randomChump( void )
{
	Zombie* zombie = newZombie(this->_values.names[rand() % this->_values.cnt_n]);
	zombie->announce();
	return zombie;
}