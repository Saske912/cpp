t_quad  *utils::new_unit(t_quad *prev)
{
    t_quad *tmp;

    tmp = new t_quad;
    tmp->prev = prev;
    tmp->next = 0;
    tmp->mt = 0;
    return tmp;
}

void    utils::copy_squad(t_quad &q1, t_quad const &q2, int count)
{
    t_quad *tmp1;
    t_quad *tmp2;
    int    i;

    i = 0;
    tmp2 = q2.next;
    tmp1 = &q1;
    while (i < count)
    {
        tmp1->next = utils::new_unit(tmp1);
        tmp1 = tmp1->next;
        tmp1->mt = tmp2->mt->clone();
        i++;
        tmp2 = tmp2->next;
    }
}

void    utils::free_squad(t_quad *core)

{
    t_quad *tmp;
    t_quad *copy;

    tmp = core;
    while (tmp)
    {
        copy = tmp->next;
        delete tmp->mt;
        delete tmp;
        tmp = copy;
    }
}

t_quad  *utils::getQuad(int idx, t_quad *materia, int cnt)
{
    t_quad  *tmp;
    int     i;

    if (idx + 1 > cnt)
        return 0;
    i = 0;
    tmp = materia;
    while (i < idx)
    {
        tmp = tmp->next;
        i++;
    }
    return tmp;
}