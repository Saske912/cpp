#include "Gost.hpp"
#include <iostream>

Gost::Gost( void ) {
}

Gost::Gost( std::string name) : Victim(name)
{
    std::cout << "Gost " << this->getName();
    std::cout << " Just appeared!" << std::endl;
}

Gost::~Gost( void ) {
}

Gost::Gost( Gost const &src ) {
    *this = src;
}

Gost &Gost::operator=( Gost const &rhs ) {
    this->setName( rhs.getName());
    return *this;
}

void    Gost::getPolymorphed() const
{
    std::cout << this->getName() << " has been turned into ";
    std::cout << "a Pokemon!" << std::endl;
}