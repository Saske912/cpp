#include <iostream>

int	ft_strlen(char *str)
{
	int	i;
	int	k;

	i = 0;
	k = 0;
	while (str[i])
	{
		k++;
		i++;
	}
	return (k);
}

char	ft_tolower(char c)
{
	char	k;

	if (c > 96 && c < 123)
		k = c - 32;
	else
		k = c;
	return (k);
}

