t_quad  *utils::new_item(t_quad *prev)
{
    t_quad *tmp;

    tmp = new t_quad;
    tmp->prev = prev;
    tmp->next = 0;
    tmp->mt = 0;
    return tmp;
}

void    utils::free_items(t_quad *core)

{
    t_quad *tmp;
    t_quad *copy;

    tmp = core;
    while (tmp)
    {
        copy = tmp->next;
        delete tmp->mt;
        delete tmp;
        tmp = copy;
    }
}

t_quad  *utils::get_item(int idx, t_quad *mt, int cnt)
{
    t_quad  *tmp;
    int     i;

    if (idx + 1 > cnt)
        return 0;
    i = 0;
    tmp = mt;
    while (i < idx)
    {
        tmp = tmp->next;
        i++;
    }
    return tmp;
}